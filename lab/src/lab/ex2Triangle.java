package lab;

public class ex2Triangle {

	public static void main(String[] args) {
		// make a triangle print from numbers
		int num = Integer.parseInt(args[0]);
		double d = 1;
		
		while (d <= num){
			//each number counting up on the same line cat ' '.
			System.out.print(d + " ");
			d++; //when num reached
					
			
			
		}
		System.out.println("");

	}

}
